package main.java.zenit.settingspanel;

/**
 * Enum representing customizable color options for the application.
 */
public enum CustomColor {

	/** Primary color used for main elements in the UI. */
	primaryColor,

	/** Lighter shade of the primary color. */
	primaryTint,

	/** Secondary color used for accents and highlights. */
	secondaryColor,

	/** Lighter shade of the secondary color. */
	secondaryTint
}
